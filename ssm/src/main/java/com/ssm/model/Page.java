package com.ssm.model;

public class Page {

	private String num;
	private String page;
	private String each;

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getPage() {
		return page;
	}

	public void setPage(String page) {
		this.page = page;
	}

	public String getEach() {
		return each;
	}

	public void setEach(String each) {
		this.each = each;
	}

}
